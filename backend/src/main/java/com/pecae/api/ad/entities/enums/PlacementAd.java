package com.pecae.api.ad.entities.enums;

/**
 * Enum que define os locais (placements) possíveis de exibição dos banners na plataforma.
 */
public enum PlacementAd {
    HOME_HERO,             // Banner principal no topo da Home
    HOME_MID,              // Banner no meio da Home
    SEARCH_TOP,            // Acima dos resultados de busca
    SEARCH_SIDEBAR,        // Lateral nos resultados de busca
    LISTING_DETAIL_TOP,    // Topo do detalhe de anúncio
    LISTING_DETAIL_BOTTOM, // Fim do detalhe de anúncio
    CATEGORY_TOP           // Topo de página de categoria
}
